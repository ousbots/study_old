package add_binary

import "testing"

func TestProvided(t *testing.T) {
	if ans := addBinary("11", "1"); ans != "100" {
		t.Fatalf("%s is not %s", ans, "100")
	}

	if ans := addBinary("1010", "1011"); ans != "10101" {
		t.Fatalf("%s is not %s", ans, "10101")
	}
}

func TestBasic(t *testing.T) {
	valid := "110111101100010011000101110110100000011101000101011001000011011000001100011110011010010011000000000"
	if ans := addBinary("10100000100100110110010000010101111011011001101110111111111101000000101111001110001111100001101", "110101001011101110001111100110001010100001101011101010000011011011001011101111001100000011011110011"); ans != valid {
		t.Fatalf("%s is not %s", ans, valid)
	}
}
